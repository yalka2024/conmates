// After the translations object definition, ensure all keys in 'en' exist in 'es', 'zh', 'hi', and 'ar'.
Object.keys(translations.en).forEach((key) => {
  ["es", "zh", "hi", "ar"].forEach((lang) => {
    const typedLang = lang as "en" | "es" | "zh" | "hi" | "ar";
    if (!(key in translations[typedLang])) {
      translations[typedLang][key] = translations.en[key];
    }
  });
});

const LanguageContext = createContext<LanguageContextType | undefined>(undefined)

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguage] = useState<Language>("en")

  useEffect(() => {
    const savedLanguage = localStorage.getItem("leaseasy-language") as Language
    if (savedLanguage && translations[savedLanguage]) {
      setLanguage(savedLanguage)
    }
  }, [])

  const handleSetLanguage = (lang: Language) => {
    setLanguage(lang)
    localStorage.setItem("leaseasy-language", lang)
    document.documentElement.dir = "ltr"
    document.documentElement.lang = lang
  }

  const t = (key: string): string => {
    // Fallback to English if the current language or key is missing
    return (
      (translations[language] && translations[language][key as keyof (typeof translations)[typeof language]]) ||
      translations["en"][key as keyof (typeof translations)["en"]] ||
      key
    )
  }

  return (
    <LanguageContext.Provider value={{ language, setLanguage: handleSetLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (context === undefined) {
    throw new Error("useLanguage must be used within a LanguageProvider")
  }
  return context
}
