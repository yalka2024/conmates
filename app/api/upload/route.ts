import { NextRequest, NextResponse } from 'next/server'
import { writeFile, mkdir } from 'fs/promises'
import { join } from 'path'
import { existsSync } from 'fs'

export async function POST(request: NextRequest) {
  try {
    const formData = await request.formData()
    const file = formData.get('file') as File | null

    if (!file) {
      return NextResponse.json({ error: 'No file provided' }, { status: 400 })
    }

    // Validate file type
    const allowedTypes = ['application/pdf', 'image/jpeg', 'image/png', 'image/jpg']
    if (!allowedTypes.includes(file.type)) {
      return NextResponse.json({ error: 'Invalid file type' }, { status: 400 })
    }

    // Validate file size (10MB max)
    const maxSize = 10 * 1024 * 1024
    if (file.size > maxSize) {
      return NextResponse.json({ error: 'File too large' }, { status: 400 })
    }

    // Create uploads directory if it doesn't exist
    const uploadsDir = join(process.cwd(), 'uploads')
    if (!existsSync(uploadsDir)) {
      await mkdir(uploadsDir, { recursive: true })
    }

    // Generate unique filename
    const timestamp = Date.now()
    const extension = file.name.split('.').pop()
    const filename = `lease_${timestamp}.${extension}`
    const filepath = join(uploadsDir, filename)

    // Convert file to buffer and save
    const bytes = await file.arrayBuffer()
    const buffer = Buffer.from(bytes)
    await writeFile(filepath, buffer)

    // Simulate AI processing (in a real app, you'd send this to your AI service)
    const analysisResult = {
      id: `analysis_${timestamp}`,
      filename: file.name,
      status: 'completed',
      summary: {
        rent: '$2,400/month',
        deposit: '$2,400',
        term: '12 months',
        keyClauses: ['Rent payment terms', 'Security deposit', 'Maintenance responsibilities'],
        redFlags: ['High late fee ($75)', 'No pet policy'],
        recommendations: ['Consider negotiating late fee', 'Ask about pet policy exceptions']
      }
    }

    return NextResponse.json({
      success: true,
      message: 'File uploaded and analyzed successfully',
      analysis: analysisResult,
      fileUrl: `/uploads/${filename}`
    })

  } catch (error) {
    console.error('Upload error:', error)
    return NextResponse.json(
      { error: 'Failed to upload file' },
      { status: 500 }
    )
  }
} 